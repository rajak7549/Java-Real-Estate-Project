    private void jLabel_nextMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_nextMouseClicked
        
        
        index++;
        if(index >= imageList.size()) index = imageList.size() - 1;
        
        displayImage(index);
        
        
        
        
    }//GEN-LAST:event_jLabel_nextMouseClicked

    private void jLabel_prevoiusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_prevoiusMouseClicked
        
        // show the previous image 
        index--;
        if(index < 0 ) index = 0;
        displayImage(index);
        
    }//GEN-LAST:event_jLabel_prevoiusMouseClicked

