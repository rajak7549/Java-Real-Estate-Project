    public PROPERTY_IMAGES_SLIDER( int pid) {
        
        initComponents();
        
        setTitle("Property Image Slider");
        
        this.propertyId = pid;

        // set border  to the jlabel image
        Border jlabel_image_border = BorderFactory.createMatteBorder(1, 1, 1, 1, new Color(0, 0, 0));
        jLabel_Image.setBorder(jlabel_image_border);

        // set border  to the next and previus jlabel image
        Border jlabel_prev_border = BorderFactory.createMatteBorder(2, 2, 2, 2, new Color(255, 69, 0));
        jLabel_prevoius.setBorder(jlabel_prev_border);
        jLabel_next.setBorder(jlabel_prev_border);

        // get the image using the hashmap 
        // Convert the Set<byte[]> returned by keySet() to an ArrayList<byte[]>
        HashMap<byte[], Integer> map = new P_PROPERTY().propertiesImageList(propertyId);
        imageList = new ArrayList<>(map.keySet());//for editing code something
        displayImage(index);
   
    }
    
    // create a function to display the image by index 
    public void displayImage( int imageIndex){
        
        Image img= new ImageIcon(imageList.get(imageIndex)).getImage().getScaledInstance(jLabel_Image.getWidth(), jLabel_Image.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image=new ImageIcon(img);
        jLabel_Image.setIcon(image);
   
    }
    
    
       

