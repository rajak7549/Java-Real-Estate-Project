    private void jButton_remove_propertyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_remove_propertyActionPerformed

        // deleting the selected property from the system
        try {
            int id = Integer.valueOf(jTextField_id.getText());

            P_PROPERTY property = new P_PROPERTY();

            if (jTextField_id.getText().trim().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the Property Id", "Empty Id", 2);
            } else {
                if (!jTextField_id.getText().trim().equals("")) {// if the type name is not empty
                    // show a confirmation message before deleting the type
                    int yes_or_no = JOptionPane.showConfirmDialog(null, "Do You Want to Delete this Property", "Delete Property", JOptionPane.YES_NO_OPTION);

                    if (yes_or_no == JOptionPane.YES_OPTION) {
                        if (property.removeProperty(id)) {
                            JOptionPane.showMessageDialog(null, "Property Data Deleted", "Deleted Property", 1);
                            
                            //clear all fields values after deleting the property
                            jTextField_id.setText("");
                            jTextArea_Address.setText(property.getAddress());
                            jTextArea_Dscription.setText("");
                            jTextField_Price.setText("");
                            jTextField_Square_feet.setText("");
                            jTextField_owner_id.setText("");
                            
                            jSpinner_Bedrooms.setValue(0);
                            jSpinner_Bathrooms.setValue(0);
                            jSpinner_Age.setValue(0);
                            
                            jCheckBox_Balconey.setSelected(false);
                            jCheckBox_Backyard.setSelected(false);
                            jCheckBox_pool.setSelected(false);
                            jCheckBox_Garage.setSelected(false);
                            
                            
                            
                        } else {
                            JOptionPane.showMessageDialog(null, "Operation failed", "Deleted Property", 2);
                        }
                    }
                }
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage() + "ERROR ", "Error", 0);

        }
    }//GEN-LAST:event_jButton_remove_propertyActionPerformed

    private void jButton_add_propertyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_add_propertyActionPerformed

        //add a new property to the system 
        // get the property data from text field
        try {
            // we need to populate the jcombobox with the tyoe name 
            //and get the value (the value is integer associated with the string)

            int type = map.get(jComboBox1_type.getSelectedItem().toString());
            int ownerId = Integer.valueOf(jTextField_owner_id.getText());
            int square_feet = Integer.valueOf(jTextField_Square_feet.getText());
            int bedrooms = Integer.valueOf(jSpinner_Bedrooms.getValue().toString());
            int bathroom = Integer.valueOf(jSpinner_Bathrooms.getValue().toString());
            int age = Integer.valueOf(jSpinner_Age.getValue().toString());

            boolean haveBalcony = false;
            boolean havePool = false;
            boolean haveBackyard = false;
            boolean haveGarage = false;

            //check if the property hacva a balcony pool backyard and garage 
            if (jCheckBox_Balconey.isSelected()) {
                haveBalcony = true;
            }
            if (jCheckBox_pool.isSelected()) {
                havePool = true;
            }
            if (jCheckBox_Backyard.isSelected()) {
                haveBackyard = true;
            }
            if (jCheckBox_Garage.isSelected()) {
                haveGarage = true;
            }

            String price = jTextField_Price.getText();
            String address = jTextArea_Address.getText();
            String description = jTextArea_Dscription.getText();

            P_PROPERTY property = new P_PROPERTY(0, type, square_feet, ownerId, price, address, bedrooms, bathroom, age, haveBalcony, havePool, haveBackyard, haveGarage, description);

            if (new P_PROPERTY().addProperty(property)) {

                JOptionPane.showMessageDialog(null, "New Property Added to the System", "Add Property", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Property Not Added to the System", "Add Property", 2);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage() + "Enter a Valid Owner id and  Square Feet Value", "Invalid ID", 2);
        }


    }//GEN-LAST:event_jButton_add_propertyActionPerformed

