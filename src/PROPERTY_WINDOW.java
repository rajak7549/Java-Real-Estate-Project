    private void jButton1_Search_PropertyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_Search_PropertyActionPerformed
        try {
            // Retrieve and validate the property ID
            String idText = jTextField_id.getText();
            if (idText.isEmpty()) {
                throw new IllegalArgumentException("Property ID cannot be empty.");
            }

            int propertyId = Integer.valueOf(idText);
            P_PROPERTY property = new P_PROPERTY().findProperty(propertyId);

            // Check if the property was found
            if (property != null) {
                // Populate the UI fields with property data
                jTextArea_Address.setText(property.getAddress());
                jTextArea_Dscription.setText(property.getDescription());
                jTextField_Price.setText(property.getPrice());
                jTextField_Square_feet.setText(String.valueOf(property.getSize()));
                jTextField_owner_id.setText(String.valueOf(property.getOwnerId()));

                // Display the property type in the combobox
                for (Object type : map.keySet()) {
                    if (map.get(type).equals(property.getType())) {
                        jComboBox1_type.setSelectedItem(type);
                        break; // Exit loop once the type is found
                    }
                }

                // Set spinner values
                jSpinner_Bedrooms.setValue(property.getBedrooms());
                jSpinner_Bathrooms.setValue(property.getBathrooms());
                jSpinner_Age.setValue(property.getAge());

                // Set checkboxes based on property features
                jCheckBox_Balconey.setSelected(property.isBalcone());
                jCheckBox_Backyard.setSelected(property.isBackyard());
                jCheckBox_pool.setSelected(property.isPool());
                jCheckBox_Garage.setSelected(property.isGarage());
            } else {
                // Show message if property not found
                JOptionPane.showMessageDialog(null, "Property Not Found", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Property ID. Please enter a valid integer.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage() + " - ERROR", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1_Search_PropertyActionPerformed

